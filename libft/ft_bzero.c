#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s_cast;
	size_t			i;

	s_cast = s;
	i = 0;
	while (i < n)
	{
		s_cast[i] = 0;
		i++;
	}
}
